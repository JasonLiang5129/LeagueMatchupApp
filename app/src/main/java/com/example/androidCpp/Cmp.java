package com.example.androidCpp;

public class Cmp {
    int cmpIcon;
    String cmpName;

    public Cmp(int cmpIcon, String cmpName) {
        this.cmpIcon = cmpIcon;
        this.cmpName = cmpName;
    }

    public int getCmpIcon() {
        return cmpIcon;
    }

    public String getCmpName() {
        return cmpName;
    }
}

